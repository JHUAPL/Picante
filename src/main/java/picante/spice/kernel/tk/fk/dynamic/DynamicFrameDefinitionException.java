package picante.spice.kernel.tk.fk.dynamic;

import picante.spice.adapters.AdapterInstantiationException;

public class DynamicFrameDefinitionException extends AdapterInstantiationException {

  /**
   * Default serial version UID.
   */
  private static final long serialVersionUID = 1L;

  public DynamicFrameDefinitionException() {
    super();
  }

  public DynamicFrameDefinitionException(String message, Throwable cause) {
    super(message, cause);
  }

  public DynamicFrameDefinitionException(String message) {
    super(message);
  }

  public DynamicFrameDefinitionException(Throwable cause) {
    super(cause);
  }
}
