  private ImmutableBiMap<Integer, EphemerisID> createStandardBindings() {

    ImmutableBiMap.Builder<Integer, EphemerisID> builder = ImmutableBiMap.builder();

    for (CelestialBodies body : CelestialBodies.values()) {
      if (body.equals(CelestialBodies.A52_EUROPA)) {
        builder.put(map.get("52_EUROPA"), body);
      }
      if (map.containsKey(body.getName())) {
        builder.put(map.get(body.getName()), body);
      }
    }

    return builder.build();

  }

